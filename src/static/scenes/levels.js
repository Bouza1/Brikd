export function returnALevel()
{
  const levelArr = []
  for(let i = 0; i < 5; i++)
    {
      let color = i % 2 === 0 ? 0xff6600 : 0x0088aa;
      const rand = alphaArr[Math.floor(Math.random() * 26)]
      const letter = allLetters[rand]
      const levelLetter = replaceColor(letter, color)
      levelArr.push(levelLetter)
    }
  return levelArr
}

function replaceColor(array, color) {
  array.forEach((row, index) => {
    row[row.length - 1] = color
  })
  return array;
}


